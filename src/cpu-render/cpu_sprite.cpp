#include "pch.h"

cpu_sprite::cpu_sprite()
{
	pTexture = nullptr;
	x = 0;
	y = 0;
	z = 0;
	anchorX = 0;
	anchorY = 0;
	index = -1;
	sortedIndex = -1;
	dead = false;
	visible = true;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void cpu_sprite::CenterAnchor()
{
	if ( pTexture )
	{
		anchorX = pTexture->width/2;
		anchorY = pTexture->height/2;
	}
}
