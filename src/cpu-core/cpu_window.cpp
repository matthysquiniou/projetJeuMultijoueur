#include "pch.h"

cpu_window::cpu_window()
{
	m_hWnd = nullptr;
}

cpu_window::~cpu_window()
{
	assert( m_hWnd==nullptr );
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void cpu_window::Initialize()
{
	s_wc.lpfnWndProc = WindowProc;
	s_wc.hInstance = GetModuleHandle(NULL);
	s_wc.lpszClassName = "cpu-engine";
	s_wc.hCursor = LoadCursor(nullptr, IDC_ARROW);
	RegisterClassA(&s_wc);
}

void cpu_window::Uninitialize()
{
	UnregisterClassA("cpu-engine", s_wc.hInstance);
	s_wc = {};
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

bool cpu_window::Create(int width, int height, bool fullscreen)
{
	if ( m_hWnd )
		return false;

	m_width = 0;
	m_height = 0;

	if ( fullscreen )
	{
		RECT rect = { 0, 0, ::GetSystemMetrics(SM_CXSCREEN), ::GetSystemMetrics(SM_CYSCREEN) };
		m_hWnd = CreateWindowA("cpu-engine", "cpu-engine", WS_POPUP, 0, 0, rect.right-rect.left, rect.bottom-rect.top, nullptr, nullptr, s_wc.hInstance, this);
	}
	else
	{
		RECT rect = { 0, 0, width, height };
		AdjustWindowRect(&rect, WS_OVERLAPPEDWINDOW, FALSE);
		m_hWnd = CreateWindowA("cpu-engine", "cpu-engine", WS_OVERLAPPEDWINDOW, CW_USEDEFAULT, 0, rect.right-rect.left, rect.bottom-rect.top, nullptr, nullptr, s_wc.hInstance, this);
	}
	if ( m_hWnd==nullptr )
		return false;

	return true;
}

void cpu_window::Quit()
{
	if ( m_hWnd )
		PostMessage(m_hWnd, WM_CLOSE, 0, 0);
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void cpu_window::Show()
{
	if ( m_hWnd==nullptr )
		return;

	ShowWindow(m_hWnd, SW_SHOW);
}

bool cpu_window::Update()
{
	if ( m_hWnd==nullptr )
		return false;

	MSG msg = {};
	while ( PeekMessage(&msg, NULL, 0, 0, PM_REMOVE) )
	{
		TranslateMessage(&msg);
		DispatchMessage(&msg);
	}

	return m_hWnd!=nullptr;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void cpu_window::Fix()
{
	if ( m_hWnd==nullptr )
		return;

	RECT rc;
	GetClientRect(m_hWnd, &rc);
	m_width = rc.right-rc.left;
	m_height = rc.bottom-rc.top;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

LRESULT cpu_window::WindowProc(HWND hWnd, UINT message, WPARAM wParam, LPARAM lParam)
{
	if ( message==WM_CREATE )
	{
		CREATESTRUCT* pCS = (CREATESTRUCT*)lParam;
		SetWindowLongPtr(hWnd, GWLP_USERDATA, (LONG_PTR)pCS->lpCreateParams);
	}

	cpu_window* pWindow = (cpu_window*)GetWindowLongPtr(hWnd, GWLP_USERDATA);
	if ( pWindow )
		return pWindow->OnWindowProc(hWnd, message, wParam, lParam);

	return DefWindowProc(hWnd, message, wParam, lParam);
}

LRESULT cpu_window::OnWindowProc(HWND hWnd, UINT message, WPARAM wParam, LPARAM lParam)
{
	switch ( message )
	{
	case WM_CREATE:
		Fix();
		break;
	case WM_DESTROY:
		m_hWnd = nullptr;
		break;
	case WM_SHOWWINDOW:
		Fix();
		break;
	case WM_SIZE:
		Fix();
		break;
	}

	if ( m_callback.IsValid() )
		m_callback.Call(message, wParam, lParam);

	return DefWindowProc(hWnd, message, wParam, lParam);
}
